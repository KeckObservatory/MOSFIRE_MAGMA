package edu.ucla.astro.irlab.mosfire.util;

public class MascgenArgumentException extends Exception {

	public MascgenArgumentException() {
		super();
	}

	public MascgenArgumentException(String message) {
		super(message);
	}

	public MascgenArgumentException(Throwable cause) {
		super(cause);
	}

	public MascgenArgumentException(String message, Throwable cause) {
		super(message, cause);
	}

}
