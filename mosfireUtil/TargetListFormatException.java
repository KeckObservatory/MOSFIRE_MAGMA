package edu.ucla.astro.irlab.mosfire.util;

public class TargetListFormatException extends Exception {

	public TargetListFormatException() {
		super();
	}

	public TargetListFormatException(String message) {
		super(message);
	}

	public TargetListFormatException(Throwable cause) {
		super(cause);
	}

	public TargetListFormatException(String message, Throwable cause) {
		super(message, cause);
	}

}
