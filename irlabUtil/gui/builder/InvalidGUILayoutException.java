package edu.ucla.astro.irlab.util.gui.builder;

public class InvalidGUILayoutException extends Exception {

	public InvalidGUILayoutException() {
	}

	public InvalidGUILayoutException(String message) {
		super(message);
	}

	public InvalidGUILayoutException(Throwable cause) {
		super(cause);
	}

	public InvalidGUILayoutException(String message, Throwable cause) {
		super(message, cause);
	}

}
