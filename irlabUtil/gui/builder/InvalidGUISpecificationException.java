package edu.ucla.astro.irlab.util.gui.builder;

public class InvalidGUISpecificationException extends Exception {

	public InvalidGUISpecificationException() {
		super();
	}

	public InvalidGUISpecificationException(String message) {
		super(message);
	}

	public InvalidGUISpecificationException(String message, Throwable cause) {
		super(message, cause);
	}

	public InvalidGUISpecificationException(Throwable cause) {
		super(cause);
	}

}
