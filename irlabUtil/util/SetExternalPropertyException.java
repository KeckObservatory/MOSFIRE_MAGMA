package edu.ucla.astro.irlab.util;

public class SetExternalPropertyException extends Exception {

	public SetExternalPropertyException() {
		super();
	}

	public SetExternalPropertyException(String message) {
		super(message);
	}

	public SetExternalPropertyException(String message, Throwable cause) {
		super(message, cause);
	}

	public SetExternalPropertyException(Throwable cause) {
		super(cause);
	}

}
